#!/usr/bin/env python3
"""
URL削除後のリマインダーメッセージサンプル表示
"""

def show_reminder_sample():
    """URL削除後のリマインダーメッセージサンプルを表示"""
    print("📨 URL削除後のリマインダーメッセージサンプル")
    print("=" * 60)

    # サンプルメッセージを手動で構築
    sample_message = """📅 【リマインダー（2日後）】

2025/10/31（木）のイベントです。
ご確認ください。

📋 **イベント詳細**
来週の練習は午前9時からです。グラウンドに集合してください。

==================================================

🌤️ **天気情報**: 当日の天気予報をご確認ください

==================================================

📋 **関連するノート**

1. 📝 来週の練習について

2. 📝 試合の調整さん"""

    print("修正後のメッセージ例:")
    print("-" * 60)
    print(sample_message)
    print("-" * 60)

    print("\n🎯 変更点まとめ:")
    print("❌ 削除された要素:")
    print("  - 🔗 ノート詳細URL")
    print("  - 🔗 ノートURL: https://...")
    print("  - 📊 調整さんURL: https://...")
    print("  - 👆 上記URLで...")

    print("\n✅ 残存する要素:")
    print("  - 📋 イベント詳細")
    print("  - 🌤️ 天気情報")
    print("  - 📋 関連するノート（タイトルのみ）")
    print("  - 📝 ノートタイトル表示")

    print("\n🎉 結果:")
    print("リマインダーメッセージがシンプルで読みやすくなりました！")
    print("URL情報は削除され、重要な情報のみが表示されます。")

if __name__ == "__main__":
    show_reminder_sample()
